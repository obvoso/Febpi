int	ft_is_prime(int nb)
{
	long long	m;

	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	if (nb == 2147483647)
		return (1);
	if ((nb % 2 == 0) || (nb % 3 == 0) || (nb % 5 == 0))
		return (0);
	m = 7;
	while (m * m <= nb)
	{
		if (nb % m == 0)
			return (0);
		++m;
	}
	return (1);
}
