int	add(int a, int b)
{
	return (a + b);
}

int min(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return (a % b);
}
