int	ft_any(char **tab, int (*f)(char*))
{	
	int	cnt;

	cnt = 0;
	while (*tab)
	{
		if (f(*tab++) != 0)
			++cnt;
	}
	if (cnt > 0)
		return (1);
	return (0);
}
