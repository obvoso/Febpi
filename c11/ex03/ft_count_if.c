int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	cnt;
	int	i;

	cnt = 0;
	i = 0;
	if (!length)
		return (0);
	while (i < length)
		if (f(tab[i++]))
			++cnt;
	return (cnt);
}
